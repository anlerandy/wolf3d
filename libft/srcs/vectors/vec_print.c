#include "vectors.h"

void	print_v2(t_vec2 a)
{
	printf("x : %5lf, y : %5lf\n", a.x, a.y);
}

void	print_v3(t_vec3 a)
{
	printf("x : %5lf, y : %5lf, z : %5lf\n", a.x, a.y, a.z);
}
