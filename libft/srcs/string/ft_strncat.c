#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	a;

	a = 0;
	i = ft_strlen(s1);
	while (s2[a] && a < n)
		s1[i++] = s2[a++];
	s1[i] = '\0';
	return (s1);
}
