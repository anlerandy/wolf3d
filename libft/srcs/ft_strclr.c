#include "libft.h"

void	ft_strclr(char *s)
{
	size_t		i;
	size_t		a;

	if (s)
	{
		a = ft_strlen(s);
		i = 0;
		while (i < a)
			s[i++] = '\0';
		s[i] = '\0';
	}
}
