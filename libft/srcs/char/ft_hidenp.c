#include <unistd.h>

int		ft_hidenp(const char *s1, const char *s2)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	if (!s1[i])
		return (1);
	while (s1[i] && s2[j])
	{
		if (s1[i] == s2[j])
			i++;
		j++;
		if (!s1[i])
			return (1);
	}
	return (0);
}
