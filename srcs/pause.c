#include "wolf.h"

void			draw_pause(t_data *data)
{
	int i;
	int j;

	j = 300;
	while (j < 500)
	{
		i = 600;
		while (i < 800)
		{
			((int*)(data->frame.img))[i + (j * data->win_w)] = 0x00FFFFFF;
			i++;
		}
		j++;
	}
}
